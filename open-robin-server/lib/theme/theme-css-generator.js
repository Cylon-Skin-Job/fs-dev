const panelCss = require('./panel-css');
const contentCss = require('./content-css');
const textCss = require('./text-css');
const borderCss = require('./border-css');
const accentCss = require('./accent-css');
const workspaceCss = require('./workspace-css');
const syntaxCss = require('./syntax-css');

function render(entry) {
  const fragments = [
    panelCss.render(entry),
    contentCss.render(entry),
    textCss.render(entry),
    borderCss.render(entry),
    accentCss.render(entry),
    workspaceCss.render(entry),
    syntaxCss.render(entry),
  ];

  return `/* Generated by theme-css-generator.js — do not edit; regenerated on theme switch. */
:root {
${fragments.join('\n')}
}`;
}

module.exports = { render };
